export type TranslationKey = keyof typeof translations.en;

export function getT(lang: Language) {
  return translations[lang] ?? translations.en;
}

export function getGreeting(lang: Language, name?: string): string {
  const t = getT(lang);
  const hour = new Date().getHours();
  let greeting = '';
  if (hour >= 5 && hour < 12) greeting = t.greetMorning;
  else if (hour >= 12 && hour < 17) greeting = t.greetAfternoon;
  else greeting = t.greetEvening;
  return name ? `${greeting}, ${name}` : greeting;
}